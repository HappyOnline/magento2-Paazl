namespace Guapa\Paazl\Model\Source\Method;

/**
 * Class Freemethod
 */
class Freemethod extends \Magento\Ups\Model\Config\Source\Method
{
    /**
     * {@inheritdoc}
     */
    public function toOptionArray()
    {
        $arr = parent::toOptionArray();
        array_unshift($arr, ['value' => '', 'label' => __('None')]);
        return $arr;
    }
}
